#include "libft.h"

int	ft_str_is_numeric(char *s)
{
	int	i;

	i = 0;
	if (*s == '-' || *s == '+')
		s++;
	while (s[i])
		if (!ft_isdigit(s[i++]))
			return (0);
	return (i > 0);
}
