#include "libft.h"

int	ft_str_is_numeric(char *s)
{
	if (*s == '-' || *s == '+')
		if (!ft_isdigit(*++s))
			return (0);
	while (ft_isdigit(*s))
		s++;
	return (*s == '\0');
}
